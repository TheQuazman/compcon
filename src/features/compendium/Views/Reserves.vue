<template>
  <div>
    <h1 class="heading mb-3 ml-5">RESERVES</h1>
    <v-tabs
      v-model="tabModel"
      background-color="primary"
      :slider-size="6"
      slider-color="active"
      fixed-tabs
    >
      <v-tab v-for="(k, i) in Object.keys(reserves)" ripple>
        {{ k }}
      </v-tab>
      <v-tab-item v-for="(k, i) in Object.keys(reserves)">
        <v-container>
          <v-row justify="center">
            <v-col v-for="reserve in reserves[k]" lg="4" md="6" sm="12">
              <reserve-card :item="reserve" />
            </v-col>
          </v-row>
        </v-container>
      </v-tab-item>
    </v-tabs>
  </div>
</template>

<script lang="ts">
import { CompendiumStore } from '@/stores';
import _ from 'lodash';

export default {
  name: 'reserves',
  data: () => ({
    tabModel: 0,
  }),
  computed: {
    reserves() {
      const compendium = CompendiumStore();
      return _.groupBy(
        compendium.Reserves.filter((x) => x),
        'Type'
      );
    },
  },
};
</script>
